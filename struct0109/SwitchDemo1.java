package com.yjl.struct0109;

public class SwitchDemo1 {

    public static void main(String[] args) {

        String name  = "顾";
        switch (name) {
            case "顾陌":
                System.out.println("顾陌");
                break;
            case"顾奈":
                System.out.println("顾奈");
                break;
            default:
                System.out.println("出错莱!");

        }
    }
}
